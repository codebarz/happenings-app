class EventList {
  constructor(id, icon, title, location, time, price, date, image, attendees) {
    this.id = id;
    this.icon = icon;
    this.title = title;
    this.location = location;
    this.time = time;
    this.price = price;
    this.date = date;
    this.image = image;
    this.attendees = attendees;
  }
}

export default EventList;